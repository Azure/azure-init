//! Unified KVP pool file backend for Hyper-V and Azure guests.
//!
//! All sizes are UTF-8 byte counts — the on-disk pool stores keys and
//! values as zero-padded UTF-8. The Windows-side Hyper-V wire spec is
//! UTF-16 char-based (256 / 1024), but the Linux `hv_kvp_daemon`
//! exchanges UTF-8 bytes with the kernel.
//!
//! Fixed-width record format (matches Linux kernel
//! `HV_KVP_EXCHANGE_MAX_*`):
//! - key field: 512 bytes
//! - value field: 2048 bytes
//! - record size: 2560 bytes
//!
//! [`PoolMode`] selects which size limits are enforced on writes:
//! - [`Safe`](PoolMode::Safe): key <= 254 bytes,
//!   value <= 1022 bytes (recommended for Linux kernel compatibility)
//! - [`Unsafe`](PoolMode::Unsafe): key <= 512 bytes, value <= 2048 bytes
//!
//! ## Reference
//! - [Hyper-V Data Exchange Service (KVP)](https://learn.microsoft.com/en-us/virtualization/hyper-v-on-windows/reference/integration-services#hyper-v-data-exchange-service-kvp)

use std::collections::{HashMap, HashSet};
use std::fs::{File, OpenOptions};
use std::io::{self, ErrorKind, Read, Seek, Write};
use std::os::unix::fs::{MetadataExt, OpenOptionsExt};
use std::os::unix::io::AsRawFd;
use std::path::{Path, PathBuf};
use std::sync::Arc;

use crate::KvpError;

/// Default base directory for KVP pool files.
const DEFAULT_KVP_DIR: &str = "/var/lib/hyperv";

const WIRE_MAX_KEY_BYTES: usize = 512;
const WIRE_MAX_VALUE_BYTES: usize = 2048;
const SAFE_MAX_KEY_BYTES: usize = 254;
const SAFE_MAX_VALUE_BYTES: usize = 1022;

/// Maximum number of unique keys allowed in the pool.
const MAX_UNIQUE_KEYS: usize = 1024;

const RECORD_SIZE: usize = WIRE_MAX_KEY_BYTES + WIRE_MAX_VALUE_BYTES;

/// Hyper-V KVP pool indices.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[repr(u8)]
pub enum KvpPool {
    /// Host-to-guest data pushed by the host admin (`.kvp_pool_0`).
    External = 0,
    /// Guest-to-host data; cloud-init / azure-init write here (`.kvp_pool_1`).
    Guest = 1,
    /// Guest intrinsics generated by the daemon (`.kvp_pool_2`).
    Auto = 2,
    /// Host-originated data describing the host (`.kvp_pool_3`).
    AutoExternal = 3,
    /// Undocumented; no pool file exists (`.kvp_pool_4`).
    AutoInternal = 4,
}

impl KvpPool {
    /// Default base directory for KVP pool files.
    pub fn default_dir() -> &'static Path {
        Path::new(DEFAULT_KVP_DIR)
    }

    /// The conventional on-disk path (`/var/lib/hyperv/<pool_file>`).
    pub fn default_path(self) -> PathBuf {
        Self::default_dir().join(self.file_name())
    }

    /// File name of the pool file, e.g. `.kvp_pool_1`.
    pub fn file_name(self) -> &'static str {
        match self {
            Self::External => ".kvp_pool_0",
            Self::Guest => ".kvp_pool_1",
            Self::Auto => ".kvp_pool_2",
            Self::AutoExternal => ".kvp_pool_3",
            Self::AutoInternal => ".kvp_pool_4",
        }
    }
}

/// Policy mode controlling key/value size limits for writes.
///
/// All limits are **UTF-8 byte counts** measured against the on-disk
/// pool format. The Windows-side Hyper-V wire spec expresses limits
/// in UTF-16 code units (256 / 1024), which equal 512 / 2048 bytes
/// when stored as UTF-8 by `hv_kvp_daemon` on Linux.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum PoolMode {
    /// Conservative limits for Linux kernel compatibility:
    /// key <= 254 UTF-8 bytes, value <= 1022 UTF-8 bytes — 2 bytes
    /// under the kernel `HV_KVP_EXCHANGE_MAX_*` maximums to leave room
    /// for a NUL terminator on either side of the boundary.
    Safe,
    /// Full Hyper-V wire-format limits expressed in UTF-8 bytes:
    /// key <= 512 bytes (== 256 UTF-16 code units),
    /// value <= 2048 bytes (== 1024 UTF-16 code units).
    Unsafe,
}

impl PoolMode {
    fn max_key_size(self) -> usize {
        match self {
            Self::Safe => SAFE_MAX_KEY_BYTES,
            Self::Unsafe => WIRE_MAX_KEY_BYTES,
        }
    }

    fn max_value_size(self) -> usize {
        match self {
            Self::Safe => SAFE_MAX_VALUE_BYTES,
            Self::Unsafe => WIRE_MAX_VALUE_BYTES,
        }
    }
}

/// Unified KVP pool file store.
#[derive(Clone, Debug)]
pub struct KvpPoolStore {
    pool: KvpPool,
    path: PathBuf,
    mode: PoolMode,
    ops: Arc<dyn SysOps>,
}

impl KvpPoolStore {
    /// Append one key-value pair to the tail of the pool without
    /// checking for an existing key.
    ///
    /// This preserves any existing records, including duplicate keys,
    /// and does not enforce [`MAX_UNIQUE_KEYS`]. Use
    /// [`insert`](Self::insert) when callers need upsert semantics.
    pub fn append(&self, key: &str, value: &str) -> Result<(), KvpError> {
        validate_key(key, self.mode.max_key_size())?;
        validate_value(value, self.mode.max_value_size())?;

        {
            let handle = self.open_for_read_write_create()?;
            let mut iter = KvpPoolIter::new(handle, false)?;
            iter.append(key, value)?;
            iter.flush()?;
        }
        Ok(())
    }

    /// Append records to the tail of the pool under one exclusive
    /// lock, preserving order and preventing cooperating writers from
    /// interleaving with the batch.
    ///
    /// Existing records are kept and duplicate keys are preserved.
    /// Like [`append`](Self::append), this does not enforce
    /// [`MAX_UNIQUE_KEYS`]; use [`populate`](Self::populate) when the
    /// caller is replacing the entire pool and wants the unique-key cap
    /// enforced. Validation happens before the file is opened; empty
    /// input is a no-op and does not create the pool file. If an I/O
    /// error occurs after writing starts, the file may contain a
    /// partial batch.
    pub fn append_multiple<I, K, V>(&self, records: I) -> Result<(), KvpError>
    where
        I: IntoIterator<Item = (K, V)>,
        K: Into<String>,
        V: Into<String>,
    {
        let records: Vec<(String, String)> = records
            .into_iter()
            .map(|(k, v)| (k.into(), v.into()))
            .collect();

        if records.is_empty() {
            return Ok(());
        }

        let max_key = self.mode.max_key_size();
        let max_value = self.mode.max_value_size();
        for (k, v) in &records {
            validate_key(k, max_key)?;
            validate_value(v, max_value)?;
        }

        self.write_records_locked(&records, false)
    }

    /// Remove all entries from the store.
    pub fn clear(&self) -> Result<(), KvpError> {
        {
            let mut handle = match self.open_for_read_write() {
                Ok(f) => f,
                Err(ref e) if e.kind() == ErrorKind::NotFound => return Ok(()),
                Err(e) => return Err(e.into()),
            };

            lock_for_writing(&mut *handle)?;
            handle.set_len(0)?;
        }
        Ok(())
    }

    /// Clear the pool file if it contains data from a previous boot.
    pub fn clear_if_stale(&self) -> Result<(), KvpError> {
        {
            let mut handle = match self.open_for_read_write() {
                Ok(f) => f,
                Err(ref e) if e.kind() == ErrorKind::NotFound => return Ok(()),
                Err(e) => return Err(e.into()),
            };

            let boot_time = boot_time(&*self.ops)?;
            lock_for_writing(&mut *handle)?;
            if handle.metadata()?.mtime <= boot_time {
                handle.set_len(0)?;
            }
        }
        Ok(())
    }

    /// Remove all records matching the key. Returns `true` if at least
    /// one record was present.
    ///
    /// Rejects empty keys and keys containing null bytes; key size is
    /// not capped, so a [`Safe`](PoolMode::Safe)-mode store can delete
    /// keys written in [`Unsafe`](PoolMode::Unsafe) mode.
    pub fn delete(&self, key: &str) -> Result<bool, KvpError> {
        validate_key(key, usize::MAX)?;

        let found = {
            let mut iter = match self.iter_mut() {
                Ok(it) => it,
                Err(KvpError::Io(e)) if e.kind() == ErrorKind::NotFound => {
                    return Ok(false);
                }
                Err(e) => return Err(e),
            };

            let mut found = false;
            while let Some(record) = iter.next() {
                let (k, _) = record?;
                if k == key {
                    iter.remove_current()?;
                    found = true;
                }
            }
            if found {
                iter.flush()?;
            }
            found
        };
        Ok(found)
    }

    /// Delete all records whose key appears in `keys`, under one
    /// exclusive lock.
    ///
    /// Returns the number of records removed, counting duplicate
    /// on-disk keys separately. Like [`delete`](Self::delete), key
    /// size is not capped. Empty input is a no-op; a missing file
    /// returns `Ok(0)`.
    ///
    /// Removal swaps deleted records with the tail, so remaining
    /// record order is not preserved.
    pub fn delete_multiple<I, K>(&self, keys: I) -> Result<usize, KvpError>
    where
        I: IntoIterator<Item = K>,
        K: Into<String>,
    {
        let mut keys_to_delete = HashSet::new();
        for key in keys {
            let key = key.into();
            validate_key(&key, usize::MAX)?;
            keys_to_delete.insert(key);
        }

        if keys_to_delete.is_empty() {
            return Ok(0);
        }

        let removed = {
            let mut iter = match self.iter_mut() {
                Ok(it) => it,
                Err(KvpError::Io(e)) if e.kind() == ErrorKind::NotFound => {
                    return Ok(0);
                }
                Err(e) => return Err(e),
            };

            let mut removed = 0usize;
            while let Some(record) = iter.next() {
                let (k, _) = record?;
                if keys_to_delete.contains(&k) {
                    iter.remove_current()?;
                    removed += 1;
                }
            }
            if removed > 0 {
                iter.flush()?;
            }
            removed
        };
        Ok(removed)
    }

    /// Return all key-value records in on-disk order, including
    /// duplicates from [`append`](Self::append) calls.
    pub fn dump(&self) -> Result<Vec<(String, String)>, KvpError> {
        let records = {
            let mut iter = match self.iter() {
                Ok(it) => it,
                Err(KvpError::Io(e)) if e.kind() == ErrorKind::NotFound => {
                    return Ok(Vec::new());
                }
                Err(e) => return Err(e),
            };

            let mut records = Vec::with_capacity(iter.record_count());
            for record in iter.by_ref() {
                records.push(record?);
            }
            records
        };
        Ok(records)
    }

    /// Return all key-value pairs (deduplicated, last-write-wins).
    pub fn entries(&self) -> Result<HashMap<String, String>, KvpError> {
        let map = {
            let mut iter = match self.iter() {
                Ok(it) => it,
                Err(KvpError::Io(e)) if e.kind() == ErrorKind::NotFound => {
                    return Ok(HashMap::new());
                }
                Err(e) => return Err(e),
            };

            let mut map = HashMap::with_capacity(iter.record_count());
            for record in iter.by_ref() {
                let (k, v) = record?;
                map.insert(k, v);
            }
            map
        };
        Ok(map)
    }

    /// Insert a new key-value pair or update an existing key's value.
    pub fn insert(&self, key: &str, value: &str) -> Result<(), KvpError> {
        validate_key(key, self.mode.max_key_size())?;
        validate_value(value, self.mode.max_value_size())?;

        {
            let handle = self.open_for_read_write_create()?;
            let mut iter = KvpPoolIter::new(handle, false)?;
            let mut found = false;
            let mut unique_keys = HashSet::new();

            // Continue past the first match to remove any duplicates
            // left by prior `append` calls so the upsert collapses to
            // a single record.
            while let Some(record) = iter.next() {
                let (k, _) = record?;
                let is_target = k == key;
                unique_keys.insert(k);
                if is_target {
                    if !found {
                        iter.overwrite_current_value(value)?;
                        found = true;
                    } else {
                        iter.remove_current()?;
                    }
                }
            }

            if !found {
                if unique_keys.len() >= MAX_UNIQUE_KEYS {
                    return Err(KvpError::MaxUniqueKeysExceeded {
                        max: MAX_UNIQUE_KEYS,
                    });
                }
                iter.append(key, value)?;
            }

            iter.flush()?;
        }
        Ok(())
    }

    /// Return whether the store is empty.
    pub fn is_empty(&self) -> Result<bool, KvpError> {
        Ok(self.len()? == 0)
    }

    /// Whether the store's data is stale (e.g. predates current boot).
    pub fn is_stale(&self) -> Result<bool, KvpError> {
        let metadata = match self.ops.path_metadata(&self.path) {
            Ok(m) => m,
            Err(ref e) if e.kind() == ErrorKind::NotFound => return Ok(false),
            Err(e) => return Err(e.into()),
        };
        Ok(metadata.mtime <= boot_time(&*self.ops)?)
    }

    /// Variant of [`is_stale`](Self::is_stale) that takes an explicit
    /// boot time, allowing tests to assert against a deterministic
    /// reference point.
    #[cfg(test)]
    fn is_stale_at_boot(&self, boot_time: i64) -> Result<bool, KvpError> {
        let metadata = match self.ops.path_metadata(&self.path) {
            Ok(m) => m,
            Err(ref e) if e.kind() == ErrorKind::NotFound => return Ok(false),
            Err(e) => return Err(e.into()),
        };
        Ok(metadata.mtime <= boot_time)
    }

    fn iter(&self) -> Result<KvpPoolIter, KvpError> {
        let handle = self.open_for_read()?;
        KvpPoolIter::new(handle, true)
    }

    fn iter_mut(&self) -> Result<KvpPoolIter, KvpError> {
        let handle = self.open_for_read_write()?;
        KvpPoolIter::new(handle, false)
    }

    /// Return the number of records in the store.
    ///
    /// This counts on-disk records, not unique keys. If
    /// [`append`](Self::append) was used to write duplicate keys, this
    /// may exceed the number of unique keys returned by
    /// [`entries`](Self::entries).
    pub fn len(&self) -> Result<usize, KvpError> {
        match self.ops.path_metadata(&self.path) {
            Ok(m) => record_count_from_len(m.len as usize).map_err(Into::into),
            Err(ref e) if e.kind() == ErrorKind::NotFound => Ok(0),
            Err(e) => Err(e.into()),
        }
    }

    /// Maximum key size in bytes for writes.
    pub fn max_key_size(&self) -> usize {
        self.mode.max_key_size()
    }

    /// Maximum value size in bytes for writes.
    pub fn max_value_size(&self) -> usize {
        self.mode.max_value_size()
    }

    /// The policy mode this store was created with.
    pub fn mode(&self) -> PoolMode {
        self.mode
    }

    /// Open using the default Hyper-V directory
    /// ([`/var/lib/hyperv`](KvpPool::default_dir)).
    pub fn new(pool: KvpPool, mode: PoolMode) -> Result<Self, KvpError> {
        Self::new_in(pool, KvpPool::default_dir(), mode)
    }

    /// Open using a custom directory (file name derived from `pool`).
    pub fn new_in(
        pool: KvpPool,
        dir: impl AsRef<Path>,
        mode: PoolMode,
    ) -> Result<Self, KvpError> {
        Self::with_ops(pool, dir, mode, Arc::new(OsSysOps::default()))
    }

    /// Internal/test constructor accepting a custom [`SysOps`].
    pub(crate) fn with_ops(
        pool: KvpPool,
        dir: impl AsRef<Path>,
        mode: PoolMode,
        ops: Arc<dyn SysOps>,
    ) -> Result<Self, KvpError> {
        let path = dir.as_ref().join(pool.file_name());
        Ok(Self {
            pool,
            path,
            mode,
            ops,
        })
    }

    fn open_for_read(&self) -> io::Result<Box<dyn Handle>> {
        self.ops.open_read(&self.path)
    }

    fn open_for_read_write(&self) -> io::Result<Box<dyn Handle>> {
        self.ops.open_read_write(&self.path)
    }

    fn open_for_read_write_create(&self) -> io::Result<Box<dyn Handle>> {
        self.ops.open_read_write_create(&self.path)
    }

    /// Shared write primitive for [`populate`](Self::populate) and
    /// [`append_multiple`](Self::append_multiple).
    ///
    /// Holds one exclusive lock across the entire batch. If
    /// `truncate` is `true`, the file is cleared first (replace
    /// semantics); otherwise records are appended to the tail.
    /// Callers are responsible for per-record validation and any
    /// `MAX_UNIQUE_KEYS` enforcement appropriate to their contract.
    fn write_records_locked(
        &self,
        records: &[(String, String)],
        truncate: bool,
    ) -> Result<(), KvpError> {
        let handle = self.open_for_read_write_create()?;
        let mut iter = KvpPoolIter::new(handle, false)?;
        if truncate {
            iter.handle.set_len(0)?;
            iter.record_count = 0;
        }
        for (k, v) in records {
            iter.append(k, v)?;
        }
        iter.flush()?;
        Ok(())
    }

    /// Return a reference to the pool file path.
    pub fn path(&self) -> &Path {
        &self.path
    }

    /// The pool this store was created for.
    pub fn pool(&self) -> KvpPool {
        self.pool
    }

    /// Replace the entire pool with the given pairs, in iteration
    /// order, under one exclusive lock.
    ///
    /// This is the inverse of [`dump`](Self::dump): duplicate keys are
    /// preserved exactly as provided, but the number of unique keys is
    /// capped at [`MAX_UNIQUE_KEYS`]. Existing records are discarded;
    /// empty input clears the pool. Use
    /// [`append_multiple`](Self::append_multiple) when callers need to
    /// extend the pool instead.
    ///
    /// Validation happens before locking, so a rejected batch never
    /// blocks other writers. The file is truncated and rewritten
    /// under one exclusive lock; an I/O error mid-write may leave
    /// the file partially written. Every successful call updates
    /// mtime (relevant to [`is_stale`](Self::is_stale)). Rejects
    /// malformed pool files (size not a multiple of the record
    /// size); call [`clear`](Self::clear) first to recover.
    pub fn populate<I, K, V>(&self, records: I) -> Result<(), KvpError>
    where
        I: IntoIterator<Item = (K, V)>,
        K: Into<String>,
        V: Into<String>,
    {
        let records: Vec<(String, String)> = records
            .into_iter()
            .map(|(k, v)| (k.into(), v.into()))
            .collect();

        let max_key = self.mode.max_key_size();
        let max_value = self.mode.max_value_size();
        let mut unique_keys = HashSet::new();
        for (k, v) in &records {
            validate_key(k, max_key)?;
            validate_value(v, max_value)?;
            unique_keys.insert(k.as_str());
        }
        if unique_keys.len() > MAX_UNIQUE_KEYS {
            return Err(KvpError::MaxUniqueKeysExceeded {
                max: MAX_UNIQUE_KEYS,
            });
        }

        self.write_records_locked(&records, true)
    }

    /// Read the value for a key. Returns `Ok(None)` when absent.
    ///
    /// If multiple records share the same key (e.g. via
    /// [`append`](Self::append)), the last (most recent) match wins.
    ///
    /// Rejects empty keys and keys containing null bytes; key size is
    /// not capped, so a [`Safe`](PoolMode::Safe)-mode store can read
    /// keys written in [`Unsafe`](PoolMode::Unsafe) mode.
    pub fn read(&self, key: &str) -> Result<Option<String>, KvpError> {
        validate_key(key, usize::MAX)?;

        let result = {
            let mut iter = match self.iter() {
                Ok(it) => it,
                Err(KvpError::Io(e)) if e.kind() == ErrorKind::NotFound => {
                    return Ok(None);
                }
                Err(e) => return Err(e),
            };

            let mut result = None;
            for record in iter.by_ref() {
                let (k, v) = record?;
                if k == key {
                    result = Some(v);
                }
            }
            result
        };
        Ok(result)
    }
}

fn boot_time(ops: &dyn SysOps) -> Result<i64, KvpError> {
    ops.boot_time().map_err(Into::into)
}

fn parse_proc_stat_btime(contents: &str) -> io::Result<i64> {
    for line in contents.lines() {
        if let Some(value) = line.strip_prefix("btime ") {
            return value.trim().parse::<i64>().map_err(|e| {
                io::Error::new(
                    ErrorKind::InvalidData,
                    format!("invalid /proc/stat btime value: {e}"),
                )
            });
        }
    }

    Err(io::Error::new(
        ErrorKind::NotFound,
        "missing btime in /proc/stat",
    ))
}

fn record_count_from_len(len: usize) -> io::Result<usize> {
    if len > 0 && !len.is_multiple_of(RECORD_SIZE) {
        return Err(io::Error::other(format!(
            "file size ({len}) is not a multiple of record size \
             ({RECORD_SIZE})"
        )));
    }
    Ok(len / RECORD_SIZE)
}

fn decode_record(data: &[u8]) -> io::Result<(String, String)> {
    if data.len() != RECORD_SIZE {
        return Err(io::Error::other(format!(
            "record size mismatch: expected {RECORD_SIZE}, got {}",
            data.len()
        )));
    }

    let (key_bytes, value_bytes) = data.split_at(WIRE_MAX_KEY_BYTES);

    let key = std::str::from_utf8(key_bytes)
        .map_err(|e| io::Error::new(ErrorKind::InvalidData, e))?
        .trim_end_matches('\0')
        .to_string();
    let value = std::str::from_utf8(value_bytes)
        .map_err(|e| io::Error::new(ErrorKind::InvalidData, e))?
        .trim_end_matches('\0')
        .to_string();

    Ok((key, value))
}

fn encode_record(key: &str, value: &str) -> Vec<u8> {
    let mut buf = vec![0u8; RECORD_SIZE];

    let key_bytes = key.as_bytes();
    let key_len = key_bytes.len().min(WIRE_MAX_KEY_BYTES);
    buf[..key_len].copy_from_slice(&key_bytes[..key_len]);

    let value_bytes = value.as_bytes();
    let value_len = value_bytes.len().min(WIRE_MAX_VALUE_BYTES);
    buf[WIRE_MAX_KEY_BYTES..WIRE_MAX_KEY_BYTES + value_len]
        .copy_from_slice(&value_bytes[..value_len]);

    buf
}

// Locking: on Linux, take both `flock` and `fcntl` OFD locks together.
//
// Linux's `flock(2)` and `fcntl(2)` byte-range locks live in disjoint
// kernel domains. `hv_kvp_daemon` uses `fcntl`; cloud-init uses
// `flock(LOCK_EX)`. Hold both to coordinate with either.
//
// Acquire `flock` first. Successful locks are released when the file
// descriptor closes. The `fcntl` OFD variant (`F_OFD_SETLKW`) shares a
// domain with classic POSIX `fcntl` byte-range locks but is per-fd and
// thread-safe.
//

/// `flock(LOCK_EX)` then `fcntl(F_WRLCK)`. If `fcntl` fails, the
/// `flock` is released before returning the error.
fn lock_for_writing(handle: &mut dyn Handle) -> io::Result<()> {
    handle.flock_exclusive()?;
    if let Err(e) = handle.fcntl_exclusive() {
        let _ = handle.flock_unlock();
        return Err(e);
    }
    Ok(())
}

/// `flock(LOCK_SH)` then `fcntl(F_RDLCK)`. See [`lock_for_writing`]
/// for the all-or-nothing semantics.
fn lock_for_reading(handle: &mut dyn Handle) -> io::Result<()> {
    handle.flock_shared()?;
    if let Err(e) = handle.fcntl_shared() {
        let _ = handle.flock_unlock();
        return Err(e);
    }
    Ok(())
}

/// Lightweight stat info shared by [`SysOps::path_metadata`] and
/// [`Handle::metadata`]. Only the fields the store actually consults.
#[derive(Clone, Copy, Debug)]
pub(crate) struct StatInfo {
    pub len: u64,
    pub mtime: i64,
}

/// Abstraction over filesystem and boot-time syscalls so tests can
/// substitute a fault-injecting backend (see `MockSysOps` in `tests`).
pub(crate) trait SysOps: std::fmt::Debug + Send + Sync {
    fn open_read(&self, path: &Path) -> io::Result<Box<dyn Handle>>;
    fn open_read_write(&self, path: &Path) -> io::Result<Box<dyn Handle>>;
    fn open_read_write_create(
        &self,
        path: &Path,
    ) -> io::Result<Box<dyn Handle>>;
    fn path_metadata(&self, path: &Path) -> io::Result<StatInfo>;
    fn boot_time(&self) -> io::Result<i64>;
}

/// Abstraction over a file handle. Every operation that can fail at
/// runtime is a trait method so tests can inject errors.
pub(crate) trait Handle:
    std::fmt::Debug + Read + Write + Seek + Send
{
    fn set_len(&mut self, len: u64) -> io::Result<()>;
    fn metadata(&mut self) -> io::Result<StatInfo>;
    fn flock_exclusive(&mut self) -> io::Result<()>;
    fn flock_shared(&mut self) -> io::Result<()>;
    fn flock_unlock(&mut self) -> io::Result<()>;
    fn fcntl_exclusive(&mut self) -> io::Result<()>;
    fn fcntl_shared(&mut self) -> io::Result<()>;
}

/// Production [`SysOps`]: real filesystem + libc syscalls.
#[derive(Debug)]
pub(crate) struct OsSysOps {
    proc_stat_path: PathBuf,
}

impl Default for OsSysOps {
    fn default() -> Self {
        Self {
            proc_stat_path: PathBuf::from("/proc/stat"),
        }
    }
}

impl SysOps for OsSysOps {
    fn open_read(&self, path: &Path) -> io::Result<Box<dyn Handle>> {
        let f = OpenOptions::new().read(true).open(path)?;
        Ok(Box::new(OsHandle(f)))
    }

    fn open_read_write(&self, path: &Path) -> io::Result<Box<dyn Handle>> {
        let f = OpenOptions::new().read(true).write(true).open(path)?;
        Ok(Box::new(OsHandle(f)))
    }

    fn open_read_write_create(
        &self,
        path: &Path,
    ) -> io::Result<Box<dyn Handle>> {
        let f = OpenOptions::new()
            .read(true)
            .write(true)
            .create(true)
            .truncate(false)
            .mode(0o600)
            .open(path)?;
        Ok(Box::new(OsHandle(f)))
    }

    fn path_metadata(&self, path: &Path) -> io::Result<StatInfo> {
        let m = std::fs::metadata(path)?;
        Ok(StatInfo {
            len: m.len(),
            mtime: m.mtime(),
        })
    }

    fn boot_time(&self) -> io::Result<i64> {
        parse_proc_stat_btime(&std::fs::read_to_string(&self.proc_stat_path)?)
    }
}

/// Production [`Handle`]: thin wrapper over [`File`].
#[derive(Debug)]
pub(crate) struct OsHandle(File);

impl Read for OsHandle {
    fn read(&mut self, buf: &mut [u8]) -> io::Result<usize> {
        self.0.read(buf)
    }
}

impl Write for OsHandle {
    fn write(&mut self, buf: &[u8]) -> io::Result<usize> {
        self.0.write(buf)
    }
    fn flush(&mut self) -> io::Result<()> {
        self.0.flush()
    }
}

impl Seek for OsHandle {
    fn seek(&mut self, pos: io::SeekFrom) -> io::Result<u64> {
        self.0.seek(pos)
    }
}

impl Handle for OsHandle {
    fn set_len(&mut self, len: u64) -> io::Result<()> {
        self.0.set_len(len)
    }

    fn metadata(&mut self) -> io::Result<StatInfo> {
        let m = self.0.metadata()?;
        Ok(StatInfo {
            len: m.len(),
            mtime: m.mtime(),
        })
    }

    fn flock_exclusive(&mut self) -> io::Result<()> {
        flock_op(&self.0, libc::LOCK_EX, "failed to flock KVP file")
    }

    fn flock_shared(&mut self) -> io::Result<()> {
        flock_op(&self.0, libc::LOCK_SH, "failed to flock KVP file")
    }

    fn flock_unlock(&mut self) -> io::Result<()> {
        flock_op(&self.0, libc::LOCK_UN, "failed to flock-unlock KVP file")
    }

    fn fcntl_exclusive(&mut self) -> io::Result<()> {
        fcntl_lock(&self.0, libc::F_WRLCK)
    }

    fn fcntl_shared(&mut self) -> io::Result<()> {
        fcntl_lock(&self.0, libc::F_RDLCK)
    }
}

fn flock_op(file: &File, op: libc::c_int, msg: &str) -> io::Result<()> {
    if unsafe { libc::flock(file.as_raw_fd(), op) } == -1 {
        return Err(io::Error::other(format!(
            "{msg}: {}",
            io::Error::last_os_error()
        )));
    }
    Ok(())
}

fn fcntl_lock(file: &File, l_type: libc::c_int) -> io::Result<()> {
    let fl = libc::flock {
        l_type: l_type as libc::c_short,
        l_whence: libc::SEEK_SET as libc::c_short,
        l_start: 0,
        l_len: 0,
        l_pid: 0,
    };
    if unsafe { libc::fcntl(file.as_raw_fd(), libc::F_OFD_SETLKW, &fl) } == -1 {
        return Err(io::Error::other(format!(
            "failed to fcntl-lock KVP file: {}",
            io::Error::last_os_error()
        )));
    }
    Ok(())
}

fn validate_key(key: &str, max: usize) -> Result<(), KvpError> {
    if key.is_empty() {
        return Err(KvpError::EmptyKey);
    }
    if key.as_bytes().contains(&0) {
        return Err(KvpError::KeyContainsNull);
    }
    let actual = key.len();
    if actual > max {
        return Err(KvpError::KeyTooLarge { max, actual });
    }
    Ok(())
}

fn validate_value(value: &str, max: usize) -> Result<(), KvpError> {
    if value.as_bytes().contains(&0) {
        return Err(KvpError::ValueContainsNull);
    }
    let actual = value.len();
    if actual > max {
        return Err(KvpError::ValueTooLarge { max, actual });
    }
    Ok(())
}

/// A record-at-a-time iterator over a KVP pool file.
///
/// Uses file I/O with seek semantics for memory-efficient iteration
/// without loading all records into memory. Supports in-place value
/// overwrites and record removal for the last-yielded record.
///
/// The underlying file is locked in [`new`](Self::new). Locks are held
/// for the iterator's lifetime and released when the iterator's file
/// descriptor is dropped.
#[derive(Debug)]
struct KvpPoolIter {
    handle: Box<dyn Handle>,
    record_count: usize,
    current_index: usize,
}

impl KvpPoolIter {
    /// Append a new record at the end of the file, zero-padded to the
    /// fixed-width wire format.
    fn append(&mut self, key: &str, value: &str) -> io::Result<()> {
        let resume_pos = self.handle.stream_position()?;
        let pos = self.handle.seek(io::SeekFrom::End(0))?;
        debug_assert!((pos as usize).is_multiple_of(RECORD_SIZE));
        self.handle.write_all(&encode_record(key, value))?;
        self.record_count += 1;
        self.handle.seek(io::SeekFrom::Start(resume_pos))?;
        Ok(())
    }

    /// Flush any buffered writes to the underlying file.
    fn flush(&mut self) -> io::Result<()> {
        self.handle.flush()
    }

    fn new(
        mut handle: Box<dyn Handle>,
        read_only: bool,
    ) -> Result<Self, KvpError> {
        if read_only {
            lock_for_reading(&mut *handle)?;
        } else {
            lock_for_writing(&mut *handle)?;
        }

        let record_count =
            record_count_from_len(handle.metadata()?.len as usize)?;
        handle.seek(io::SeekFrom::Start(0))?;

        Ok(Self {
            handle,
            record_count,
            current_index: 0,
        })
    }

    /// Overwrite the value field of the record last returned by
    /// [`next()`](Iterator::next), zero-padding to fill the
    /// fixed-width 2048-byte field.
    ///
    /// After the write the file position is at the start of the next
    /// record, so iteration can continue normally.
    ///
    /// Returns `InvalidInput` if called before the first `next()`.
    fn overwrite_current_value(&mut self, value: &str) -> io::Result<()> {
        if self.current_index == 0 {
            return Err(io::Error::new(
                ErrorKind::InvalidInput,
                "no record has been read yet",
            ));
        }

        let record_start = (self.current_index - 1) as u64 * RECORD_SIZE as u64;
        let value_offset = record_start + WIRE_MAX_KEY_BYTES as u64;

        self.handle.seek(io::SeekFrom::Start(value_offset))?;

        let mut buf = [0u8; WIRE_MAX_VALUE_BYTES];
        let bytes = value.as_bytes();
        let len = bytes.len().min(WIRE_MAX_VALUE_BYTES);
        buf[..len].copy_from_slice(&bytes[..len]);

        self.handle.write_all(&buf)?;
        Ok(())
    }

    /// The number of records in the file (updated by
    /// [`append`](Self::append) and
    /// [`remove_current`](Self::remove_current)).
    fn record_count(&self) -> usize {
        self.record_count
    }

    /// Remove the record last returned by [`next()`](Iterator::next)
    /// by swapping it with the final record and truncating the file.
    ///
    /// After removal the file position rewinds so that the next call
    /// to `next()` reads the record that was swapped into this slot
    /// (unless the removed record was already the last one).
    ///
    /// Returns `InvalidInput` if called before the first `next()`.
    fn remove_current(&mut self) -> io::Result<()> {
        if self.current_index == 0 {
            return Err(io::Error::new(
                ErrorKind::InvalidInput,
                "no record has been read yet",
            ));
        }

        let delete_index = self.current_index - 1;
        let last_index = self.record_count - 1;

        if delete_index != last_index {
            self.handle.seek(io::SeekFrom::Start(
                last_index as u64 * RECORD_SIZE as u64,
            ))?;
            let mut buf = [0u8; RECORD_SIZE];
            self.handle.read_exact(&mut buf)?;

            self.handle.seek(io::SeekFrom::Start(
                delete_index as u64 * RECORD_SIZE as u64,
            ))?;
            self.handle.write_all(&buf)?;

            // Rewind so the next `next()` re-reads `delete_index`,
            // which now holds the swapped-in record. `write_all`
            // advanced the cursor past it, hence this third seek.
            self.handle.seek(io::SeekFrom::Start(
                delete_index as u64 * RECORD_SIZE as u64,
            ))?;
            self.current_index = delete_index;
        }
        // No-swap branch: `current_index` is left at the old
        // `record_count`, so after the truncate below the iterator
        // is naturally exhausted and the next `next()` returns None.

        self.handle
            .set_len(last_index as u64 * RECORD_SIZE as u64)?;
        self.record_count -= 1;
        Ok(())
    }
}

impl Iterator for KvpPoolIter {
    type Item = io::Result<(String, String)>;

    fn next(&mut self) -> Option<Self::Item> {
        if self.current_index >= self.record_count {
            return None;
        }
        let mut buf = [0u8; RECORD_SIZE];
        match self.handle.read_exact(&mut buf) {
            Ok(()) => {
                self.current_index += 1;
                Some(decode_record(&buf))
            }
            Err(e) => Some(Err(e)),
        }
    }

    fn size_hint(&self) -> (usize, Option<usize>) {
        let remaining = self.record_count - self.current_index;
        (remaining, Some(remaining))
    }
}

